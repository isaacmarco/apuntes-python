# bucle while + continue y break
x = 0
while x < 10:
    pass
    break
    continue


# bucles for
palabra = 'cosa'
for p in palabra:
    pass

# comprobar si un bucle ha sido interrumpido
for p in palabra:
    break
else:
    print('el bucle ha sido interrumpido')

# for con range(inicio, fin, paso)
for i in range(3):
    pass

# saltando de dos en dos
for i in range(0, 10, 2):
    pass

# recorriendo hacia atras de uno en uno
for i in range(10, -1, -1):
    pass

# recorrido anonimo con _
for _ in range(10):
    pass



